package org.example

fun main() {
    println("Hello world!")
    val byteNum: Byte = 50
    val shortNum: Short = 130
    val intNum: Int = 35000
    val longNum: Long = 25446321321321
    println(byteNum)
    println(shortNum)
    println(intNum)
    println(longNum)

    val weight: Float = 5.5f
    val height: Double = 172.68
    val weight2: Float = 5.4E6f
    val height2: Double = 172.68e3
    println(weight)
    println(height)
    println(weight2)
    println(height2)

    val isSucceed: Boolean = true
    val isMonday = false
    println(isSucceed)
    println(isMonday)

    val grade: Char = 'A'
    val gender = 'm'
    println(gender)
    println(grade)
}